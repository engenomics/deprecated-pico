package org.engenomics.pico;

public class Coords {
    private int c1;
    private int c2;

    public Coords(int c1, int c2) {

        this.c1 = c1;
        this.c2 = c2;
    }

    public int getC1() {

        return c1;
    }

    public void setC1(int c1) {
        this.c1 = c1;
    }

    public int getC2() {
        return c2;
    }

    public void setC2(int c2) {
        this.c2 = c2;
    }
}
